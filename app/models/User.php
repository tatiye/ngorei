<?php
  class User {
    private $db;

    public function __construct(){
      $this->db = new Database;
    }

    // Add User / Register
    public function register($data){
      // Prepare Query
      $this->db->query('INSERT INTO appuser (fullname, username ,password) 
      VALUES (:fullname, :username, :password)');

      // Bind Values
      $this->db->bind(':fullname', $data['name']);
      $this->db->bind(':username', $data['email']);
      $this->db->bind(':password', $data['password']);
      //Execute
      if($this->db->execute()){
        return true;
      } else {
        return false;
      }
    }

    // Find USer BY Email
    public function findUserByEmail($email){
      $this->db->query("SELECT * FROM appuser WHERE username = :username");
      $this->db->bind(':username', $email);

      $row = $this->db->single();

      //Check Rows
      if($this->db->rowCount() > 0){
        return true;
      } else {
        return false;
      }
    }

    // Login / Authenticate User
    public function login($email, $password){
      $this->db->query("SELECT * FROM appuser WHERE username = :username");
      $this->db->bind(':username', $email);

      $row = $this->db->single();
      
      $hashed_password = $row->password;
      if(password_verify($password, $hashed_password)){
        return $row;
      } else {
        return false;
      }
    }

    // Find User By ID
    public function getUserById($id){
      $this->db->query("SELECT * FROM users WHERE id = :id");
      $this->db->bind(':id', $id);
      $row = $this->db->single();
      return $row;
    }
  }